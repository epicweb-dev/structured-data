// User Management System
// Creating typed objects

const user = {
	name: 'Alice',
	age: 30,
	email: 'alice@example.com',
}

const admin = {
	name: 'Bob',
	age: 35,
	email: 'bob@example.com',
}

console.log(`User: ${user.name}, Age: ${user.age}`)
console.log(`Admin: ${admin.name}, Age: ${admin.age}`)

export { user, admin }
